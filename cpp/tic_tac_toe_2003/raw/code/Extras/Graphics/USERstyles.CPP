#include<graphics.h>
#include<stdlib.h>
#include<conio.h>
void main()
{
int gd=DETECT,gm,maxx,maxy,x=40,y=40,fst;
char str[40];
char patterns[][8]=
	{
		{0xAA,0x55,0xAA,0x55,0xAA,0x55,0xAA,0x55},
		{0x33,0x33,0xCC,0xCC,0x33,0x33,0xCC,0xCC},
		{0xF0,0xF0,0xF0,0xF0,0x0F,0x0F,0x0F,0x0F},
		{0x00,0x10,0x28,0x44,0x28,0x10,0x00,0x00},
		{0x00,0x70,0x20,0x27,0x24,0x24,0x07,0x00},
		{0x00,0x00,0x00,0x18,0x18,0x00,0x00,0x00},
		{0x00,0x00,0x3C,0x3C,0x3C,0x3C,0x00,0x00},
		{0x00,0x7E,0x7E,0x7E,0x7E,0x7E,0x7E,0x00},
		{0x00,0x00,0x22,0x08,0x00,0x22,0x1C,0x00},
		{0xFF,0x7E,0x3C,0x18,0x18,0x3C,0x7E,0xFF},
		{0x00,0x10,0x10,0x7C,0x10,0x10,0x00,0x00},
		{0x00,0x42,0x24,0x18,0x18,0x24,0x42,0x00}
	};

initgraph(&gd,&gm,"d:\\tc\\bgi");

maxx=getmaxx();
maxy=getmaxy();
rectangle(0,10,maxx,maxy);

setcolor(WHITE);
outtextxy(175,0,"User-defined fill styles");
for(fst=0;fst<12;fst++)
	{
	setfillpattern(&patterns[fst][0],MAGENTA);
	bar(x,y,x+80,y+80);
	rectangle(x,y,x+80,y+80);

	itoa(fst,str,10);
	outtextxy(x,y+100,str);

	x=x+150;
	if(x>490)
		{
		y=y+150;
		x=40;
		}
	}

getch();
closegraph();
restorecrtmode();
}
